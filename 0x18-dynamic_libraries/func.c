#include "func.h"

/**
 * add - add two numbers
 * @a: a
 * @b: b
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: a
 * @b: b
 *
 * Return: value of a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: a
 * @b: b
 *
 * Return: multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: a
 * @b: b
 *
 * Return: division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modular of a and b
 * @a: a
 * @b: b
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
